class assignment_operator {
    public static void main(String[] args) {
        int num1 = 7;
        int num2 = 5;
        int result = num1 + num2;
        System.out.println(result);

        int num11 = 7;
        int num21 = 5;
        int result1 = num11 - num21;
        System.out.println(result1);

        int num12 = 7;
        int num22 = 5;
        int result2 = num12 * num22;
        System.out.println(result2);


        int num13 = 7;
        int num23 = 5;
        int result3 = num13 / num23;
        System.out.println(result3);


        int num14 = 7;
        int num24 = 5;
        int result4 = num14 % num24;
        System.out.println(result4);


        int num15 = 7;
        // int num25 = 5;
        num15 += 4;
        System.out.println(num15);

        int num16 = 7;
        // int num25 = 5;
        num16 -= 4;
        System.out.println(num16);
    }    
}
